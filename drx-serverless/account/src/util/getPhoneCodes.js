function getCountryCodeByIsoNumeric(isoNumeric, countryCodeDefault) {
  console.log('isoNumeric');
  console.log(isoNumeric);
  console.log('countryCodeDefault');
  console.log(countryCodeDefault);

  const phoneCodes = getPhoneCodes();
  const matchingCountries = phoneCodes.filter(
    (code) => code.isoNumeric === isoNumeric
  );

  console.log('matchingCountries');
  console.log(matchingCountries);

  // Check if multiple matches exist
  if (matchingCountries.length > 1) {
    // Return the default countryCode if it exists among the matches
    const defaultMatch = matchingCountries.find(
      (code) =>
        code.countryCode.toLowerCase() === countryCodeDefault.toLowerCase()
    );

    return defaultMatch
      ? defaultMatch.countryCode
      : matchingCountries[0].countryCode;
  }

  // If only one match or none found, return the countryCode or null
  return matchingCountries.length === 1
    ? matchingCountries[0].countryCode
    : null;
}

